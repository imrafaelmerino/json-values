  private List<SpecError> test(JsPath parent,
                               List<SpecError> errors,
                               JsValue parentValue
                              ) {

    if (parentValue.isNull() && nullable) {
      return errors;
    }
    if (!parentValue.isObj()) {
      errors.add(SpecError.of(parent,
                              new JsError(parentValue,
                                          OBJ_EXPECTED
                              )
                             ));
      return errors;
    }
    JsObj json = parentValue.toJsObj();
    if (metaData != null) {
      if (json.size() < metaData.minProperties()) {
        errors.add(SpecError.of(parent,
                                new JsError(parentValue,
                                            ERROR_CODE.OBJ_MIN_SIZE_NOT_MET

                                )
                               ));
      }
      if (json.size() > metaData.maxProperties()) {
        errors.add(SpecError.of(parent,
                                new JsError(parentValue,
                                            ERROR_CODE.OBJ_MAX_SIZE_EXCEEDED
                                )
                               ));
      }
    }
    for (var next : json) {
      String key = next.key();
      JsValue value = next.value();
      JsPath keyPath = JsPath.fromKey(key);
      JsPath currentPath = parent.append(keyPath);
      JsSpec spec = getSpec(key);

      if (spec != null) {
        errors.addAll(spec.test(currentPath,
                                value));
      } else if (strict) {
        errors.add(SpecError.of(currentPath,
                                new JsError(value,
                                            SPEC_MISSING)));
      }

    }

    for (String requiredField : requiredFields) {
      if (!json.containsKey(requiredField) && !containAnAlias(json,
                                                              requiredField,
                                                              metaData)) {
        errors.add(SpecError.of(parent.key(requiredField),
                                new JsError(JsNothing.NOTHING,
                                            REQUIRED
                                )
                               )
                  );
      }
    }

    if (predicate != null && !predicate.test(json)) {
      errors.add(SpecError.of(JsPath.empty(),
                              new JsError(json,
                                          OBJ_CONDITION
                              )
                             )
                );
    }

    return errors;
  }

  JsSpec getSpec(String key) {
    JsSpec spec = bindings.get(key);
    if (spec != null) {
      return spec;
    }
    String aliasField = metaData != null ? metaData.getAliasField(key) : null;
    if (aliasField != null) {
      return bindings.get(aliasField);
    }
    return null;
  }

  private boolean containAnAlias(JsObj json,
                                 String key,
                                 MetaData metaData) {
    if (metaData == null) {
      return false;
    }
    if (metaData.fieldsAliases() == null) {
      return false;
    }
    if (!metaData.fieldsAliases()
                 .containsKey(key)) {
      return false;
    }
    return metaData.fieldsAliases()
                   .get(key)
                   .stream()
                   .anyMatch(json::containsKey);
  }

