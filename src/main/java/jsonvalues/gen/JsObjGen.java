  /**
   * Concatenates the JSON objects generated by this generator with the JSON objects generated by the specified
   * generator. This operation combines the optional, nullable, and binding configurations of both generators. This
   * method combines the optional and nullable fields of both generators. Optional fields are those that may or may not
   * be present in the generated JSON objects, while nullable fields are those that may have null values. The bindings,
   * representing key-value pairs, are merged, and conflicts are resolved by favoring the values from the specified
   * generator. This is particularly useful when combining JSON object generators that represent different aspects of a
   * larger data structure, allowing for a unified generator with the combined configuration.
   *
   * @param gen The generator providing JSON objects to concatenate with the JSON objects generated by this generator.
   * @return A new generator that produces JSON objects resulting from the concatenation.
   * @throws NullPointerException if the specified generator is null.
   */
  public JsObjGen concat(final JsObjGen gen) {
    requireNonNull(gen);
    var xs = new HashSet<>(optionals);
    var ys = new HashSet<>(nullables);
    var zs = new HashMap<>(bindings);

    xs.addAll(gen.optionals);
    ys.addAll(gen.nullables);
    zs.putAll(gen.bindings);

    return new JsObjGen(zs,
                        xs,
                        ys);

  }

